# Public: Decides the smallest number 
#
# number1 - The Integer that is the first value  
# number2 - The Integer that is the second value
# 
#
# Examples
#
#   Min_of_Two(100.20)
#   # => 20
#
# Returns the smallest number
def Min_of_Two(number1,number2)
    if number1 > number2
        output = number2
    else
         output = number1
    end
    return output
end




    
